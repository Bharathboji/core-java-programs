public class swapnumbers{
     public static void main(String[] args) {
        int a=30,b=10;
        System.out.println("Before swap");
        System.out.println(a);
        System.out.println(b);

        int temp=a;
            a=b;
            b=temp;
          System.out.println("After swap");
          System.out.println(a);
          System.out.println(b);  

    }
}